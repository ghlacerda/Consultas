	   --[MOTORISTA] AS colaborador_NAME,
		   REPLACE(REPLACE(CGCCPF,'.',''),'-','') AS colaborador_identification,
		   301 AS indicador_identification,
		   Sum(QTDE) AS resultado,
		   Sum(TOTAL) AS fator_0,
		   0 AS fator_1,
		   0 AS fator_2,
