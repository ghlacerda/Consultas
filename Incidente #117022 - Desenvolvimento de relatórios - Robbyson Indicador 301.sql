		   0 AS fator_7,
		   0 AS fator_8,
		   0 AS fator_9,
		   0 AS fator_10,
		   CAST([DATARETORNO] AS DATE) AS [DATA]

  From (
  
  Select GLOP_VIAGEMDOCUMENTOS.FILIALORIGEM						[FILIAL],
			   CPF.CGCCPF,
			   CPF.NOME							[MOTORISTA],
			   CPF.HANDLE,
			   GLOP_RADIOMENSAGENS.MOTORISTA IDMOTORISTA,
			   Cast(GLOP_RADIOMENSAGENS.DATAHORARETORNO As Date)		[DATARETORNO],
			   Concat(Replicate('0', 2 - Len(DatePart(Hour, GLOP_RADIOMENSAGENS.DATAHORARETORNO))), DatePart(Hour, GLOP_RADIOMENSAGENS.DATAHORARETORNO))		[Hora],
			   IIF(Count(Distinct GLOP_RADIOMENSAGENS.ENDERECO) > 20, Count(Distinct GLOP_RADIOMENSAGENS.ENDERECO), 0)											[QTDE],
			   Count(Distinct GLOP_RADIOMENSAGENS.ENDERECO)																										[TOTAL]
		  From GLOP_RADIOMENSAGENS
		 Inner Join GLOP_RADIOOCORRENCIAS	On GLOP_RADIOOCORRENCIAS.HANDLE					= GLOP_RADIOMENSAGENS.OCORRENCIAASSOCIADA
		 Inner Join GLOP_VIAGEMDOCUMENTOS	On GLOP_VIAGEMDOCUMENTOS.HANDLE					= GLOP_RADIOMENSAGENS.DOCUMENTOVIAGEM
										   And GLOP_VIAGEMDOCUMENTOS.DOCUMENTOLOGISTICA		= GLOP_RADIOMENSAGENS.DOCUMENTOLOGISTICA
		 LEFT JOIN GN_PESSOAS CPF ON GLOP_RADIOMENSAGENS.MOTORISTA = CPF.HANDLE
		 LEFT JOIN GLOP_VIAGENS VG ON GLOP_VIAGEMDOCUMENTOS.VIAGEM = VG.HANDLE

		 Where Cast(GLOP_RADIOMENSAGENS.DATAHORARETORNO As Date) >= DATEADD(DD, -1, CAST(GETDATE() AS DATE))
		 AND   Cast(GLOP_RADIOMENSAGENS.DATAHORARETORNO As Date) < CAST(GETDATE() AS DATE)
		 And GLOP_RADIOOCORRENCIAS.OCORRENCIA												In (1, 22776, 63, 87)   

		 Group By Cast(GLOP_RADIOMENSAGENS.DATAHORARETORNO As Date),
				  CPF.CGCCPF,
			      CPF.NOME,
			      CPF.HANDLE,
			      GLOP_RADIOMENSAGENS.MOTORISTA,
				  DatePart(Hour, GLOP_RADIOMENSAGENS.DATAHORARETORNO),
				  GLOP_VIAGEMDOCUMENTOS.FILIALORIGEM
				  ) TB

